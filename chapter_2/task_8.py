digit_day = int(input("Please input digit from 1 to 7:"))

if digit_day == 1:
    print("Monday")
elif digit_day == 2:
    print("Tuesday")
elif digit_day == 3:
    print("Wednesday")
elif digit_day == 4:
    print("Thursday")
elif digit_day == 5:
    print("Friday")
elif digit_day == 6:
    print("Saturday")
elif digit_day == 7:
    print("Sunday")
